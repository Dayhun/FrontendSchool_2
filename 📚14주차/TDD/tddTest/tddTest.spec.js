// describe : 테스트 유닛(it)들의 모음
describe('자스민 테스트입니다.', () => {
    // it : 함수 테스트 유닛입니다.
    it('더하기 1을 하는 함수입니다.', () => {
        let num = 30;
        // 기대식 :  실행할 함수(plusOne)의 결과값을 인수로 전달합니다. 이 함수가 어떻게 되길 기대해?
        // toBe : 매치함수입니다. 내가 기대한 결과와 일치하는지 판단하는 함수. (이 함수가 이게 되어야한다고 기대하는 것.)
        expect(plusOne(num)).toBe(num + 1);

    })
});